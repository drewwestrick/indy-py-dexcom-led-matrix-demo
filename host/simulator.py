class MockPicoGraphics:
    """Mock PicoGraphics class for simulation with proper font6 rendering"""
    
    def __init__(self, display_type):
        self.width = WIDTH
        self.height = HEIGHT
        self.pixels = [[0 for _ in range(WIDTH)] for _ in range(HEIGHT)]
        self.current_pen = (255, 255, 255)
        
    def set_pen(self, r, g, b):
        """Set the current drawing color"""
        self.current_pen = (r, g, b)
        
    def clear(self):
        """Clear the display to black"""
        self.pixels = [[0 for _ in range(WIDTH)] for _ in range(HEIGHT)]
        
    def pixel(self, x, y):
        """Set a single pixel"""
        if 0 <= x < WIDTH and 0 <= y < HEIGHT:
            self.pixels[y][x] = self.current_pen
            
    def text(self, text_str, x, y, scale=1):
        """
        Render text using font6 bitmap font (matching Pimoroni implementation)
        """
        offset_x = 0
        
        for char in text_str:
            # Get character index (ASCII)
            char_idx = ord(char) - 32  # Font starts at space (ASCII 32)
            
            if char_idx < 0 or char_idx >= len(FONT6_DATA):
                continue  # Skip unknown characters
                
            # Get character width and bitmap data
            char_width = FONT6_WIDTHS[char_idx]
            char_data = FONT6_DATA[char_idx]
            
            # Draw each column of the character
            for col in range(char_width):
                column_data = char_data[col]
                
                # Draw each bit in the column (6 pixels high)
                for row in range(6):
                    if column_data & (1 << row):  # Check if bit is set
                        # Draw scaled pixel
                        for sy in range(scale):
                            for sx in range(scale):
                                px = x + offset_x + (col * scale) + sx
                                py = y + (row * scale) + sy
                                if 0 <= px < WIDTH and 0 <= py < HEIGHT:
                                    self.pixels[py][px] = self.current_pen
            
            # Move to next character position (width + 1 pixel spacing)
            offset_x += (char_width + 1) * scale
    
    def update(self):
        """Called to refresh display (no-op in simulator)"""
        pass


