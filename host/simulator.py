class DisplaySimulator:
    """Main simulator class"""
    
    def __init__(self):
        pygame.init()
        self.screen = pygame.display.set_mode((WINDOW_WIDTH, WINDOW_HEIGHT))
        pygame.display.set_caption("Galactic Unicorn Simulator (53x11)")
        self.clock = pygame.time.Clock()
        self.graphics = MockPicoGraphics(None)
        self.running = True
        
    def draw_display(self, graphics):
        """Render the graphics buffer to the pygame window"""
        self.screen.fill((0, 0, 0))
        
        # Draw each pixel
        for y in range(HEIGHT):
            for x in range(WIDTH):
                pixel_value = graphics.pixels[y][x]
                if pixel_value == 0:
                    color = (0, 0, 0)
                else:
                    color = pixel_value
                    
                rect = pygame.Rect(
                    x * PIXEL_SIZE,
                    y * PIXEL_SIZE,
                    PIXEL_SIZE - 1,  # -1 for grid lines
                    PIXEL_SIZE - 1
                )
                pygame.draw.rect(self.screen, color, rect)
        
        # Draw grid lines (optional, helpful for layout)
        for x in range(0, WINDOW_WIDTH, PIXEL_SIZE):
            pygame.draw.line(self.screen, (30, 30, 30), (x, 0), (x, WINDOW_HEIGHT))
        for y in range(0, WINDOW_HEIGHT, PIXEL_SIZE):
            pygame.draw.line(self.screen, (30, 30, 30), (0, y), (WINDOW_WIDTH, y))
            
        pygame.display.flip()
        
    def handle_events(self):
        """Handle pygame events"""
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                self.running = False
            elif event.type == pygame.KEYDOWN:
                if event.key == pygame.K_ESCAPE or event.key == pygame.K_q:
                    self.running = False
                    
    def run(self, draw_callback):
        """Run the simulator with a custom draw callback"""
        while self.running:
            self.handle_events()
            
            # Clear and redraw
            self.graphics.clear()
            draw_callback(self.graphics)
            
            # Render to screen
            self.draw_display(self.graphics)
            
            # Limit to 60 FPS
            self.clock.tick(60)
            
        pygame.quit()


